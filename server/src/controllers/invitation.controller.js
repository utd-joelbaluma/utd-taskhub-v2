import { supabase } from "../config/supabase.js";
import { env } from "../config/env.js";
import { sendInvitationEmail } from "../services/mailer.service.js";
import {
	validateSendInvitation,
	validateAcceptInvitation,
} from "../utils/invitation.validator.js";

export async function sendInvitation(req, res, next) {
	try {
		const projectId = req.params.id;
		const { email, role = "member" } = req.body;

		const errors = validateSendInvitation(req.body);
		if (errors.length > 0) {
			return res.status(400).json({
				success: false,
				message: "Validation failed.",
				errors,
			});
		}

		const normalizedEmail = email.trim().toLowerCase();

		// Verify project exists
		const { data: project, error: projectError } = await supabase
			.from("projects")
			.select("id, name")
			.eq("id", projectId)
			.maybeSingle();

		if (projectError) throw projectError;
		if (!project) {
			return res.status(404).json({
				success: false,
				message: "Project not found.",
			});
		}

		// Check if the email belongs to an existing user who is already a member
		const { data: existingProfile } = await supabase
			.from("profiles")
			.select("id")
			.eq("email", normalizedEmail)
			.maybeSingle();

		if (existingProfile) {
			const { data: existingMember } = await supabase
				.from("project_members")
				.select("id")
				.eq("project_id", projectId)
				.eq("user_id", existingProfile.id)
				.maybeSingle();

			if (existingMember) {
				return res.status(409).json({
					success: false,
					message: "This user is already a member of the project.",
				});
			}
		}

		// Check for an active pending invitation for this email + project
		const { data: existingInvite } = await supabase
			.from("invitations")
			.select("id")
			.eq("project_id", projectId)
			.eq("email", normalizedEmail)
			.eq("status", "pending")
			.gt("expires_at", new Date().toISOString())
			.maybeSingle();

		if (existingInvite) {
			return res.status(409).json({
				success: false,
				message: "A pending invitation for this email already exists.",
			});
		}

		// Create the invitation — token is auto-generated by the DB default
		const { data: invitation, error: inviteError } = await supabase
			.from("invitations")
			.insert({
				project_id: projectId,
				invited_by: req.profile.id,
				email: normalizedEmail,
				role,
			})
			.select()
			.single();

		if (inviteError) throw inviteError;

		const inviteUrl = `${env.appUrl}/invitations/accept?token=${invitation.token}`;

		await sendInvitationEmail({
			to: normalizedEmail,
			invitedByName: req.profile.full_name || req.profile.email,
			projectName: project.name,
			role,
			inviteUrl,
		});

		res.status(201).json({
			success: true,
			message: `Invitation sent to ${normalizedEmail}.`,
			data: {
				invitation,
			},
		});
	} catch (error) {
		next(error);
	}
}

export async function listInvitations(req, res, next) {
	try {
		const projectId = req.params.id;
		const { status } = req.query;

		let query = supabase
			.from("invitations")
			.select(
				`
        id,
        email,
        role,
        status,
        expires_at,
        accepted_at,
        created_at,
        invited_by:profiles!invitations_invited_by_fkey (
          id,
          full_name,
          email
        )
      `
			)
			.eq("project_id", projectId)
			.order("created_at", { ascending: false });

		if (status) {
			query = query.eq("status", status);
		}

		const { data, error } = await query;
		if (error) throw error;

		res.status(200).json({
			success: true,
			count: data.length,
			data,
		});
	} catch (error) {
		next(error);
	}
}

export async function cancelInvitation(req, res, next) {
	try {
		const projectId = req.params.id;
		const invitationId = req.params.invitationId;

		const { data: invitation, error: findError } = await supabase
			.from("invitations")
			.select("id, status")
			.eq("id", invitationId)
			.eq("project_id", projectId)
			.maybeSingle();

		if (findError) throw findError;

		if (!invitation) {
			return res.status(404).json({
				success: false,
				message: "Invitation not found.",
			});
		}

		if (invitation.status !== "pending") {
			return res.status(400).json({
				success: false,
				message: `Invitation cannot be cancelled. Current status: ${invitation.status}.`,
			});
		}

		const { error: updateError } = await supabase
			.from("invitations")
			.update({ status: "cancelled" })
			.eq("id", invitationId);

		if (updateError) throw updateError;

		res.status(200).json({
			success: true,
			message: "Invitation cancelled.",
		});
	} catch (error) {
		next(error);
	}
}

// Public endpoint — no auth required. Token is the credential.
export async function acceptInvitation(req, res, next) {
	try {
		const errors = validateAcceptInvitation(req.body);
		if (errors.length > 0) {
			return res.status(400).json({
				success: false,
				message: "Validation failed.",
				errors,
			});
		}

		const { token } = req.body;

		const { data: invitation, error: findError } = await supabase
			.from("invitations")
			.select("*")
			.eq("token", token)
			.maybeSingle();

		if (findError) throw findError;

		if (!invitation) {
			return res.status(404).json({
				success: false,
				message: "Invalid invitation token.",
			});
		}

		// Mark expired tokens as expired on first check
		if (new Date(invitation.expires_at) < new Date()) {
			if (invitation.status === "pending") {
				await supabase
					.from("invitations")
					.update({ status: "expired" })
					.eq("id", invitation.id);
			}
			return res.status(410).json({
				success: false,
				message: "This invitation has expired.",
			});
		}

		if (invitation.status === "accepted") {
			return res.status(409).json({
				success: false,
				message: "This invitation has already been accepted.",
			});
		}

		if (invitation.status === "cancelled") {
			return res.status(400).json({
				success: false,
				message: "This invitation has been cancelled.",
			});
		}

		// Find the profile for the invited email
		const { data: profile, error: profileError } = await supabase
			.from("profiles")
			.select("id")
			.eq("email", invitation.email)
			.maybeSingle();

		if (profileError) throw profileError;

		// If no account exists yet, tell the client to register first
		if (!profile) {
			return res.status(200).json({
				success: false,
				requires_registration: true,
				message: "Please create an account to accept this invitation.",
				data: {
					email: invitation.email,
					token,
				},
			});
		}

		// Check if the user is already a project member
		const { data: existingMember } = await supabase
			.from("project_members")
			.select("id")
			.eq("project_id", invitation.project_id)
			.eq("user_id", profile.id)
			.maybeSingle();

		if (existingMember) {
			// Still mark the invitation accepted so it doesn't remain pending
			await supabase
				.from("invitations")
				.update({ status: "accepted", accepted_at: new Date().toISOString() })
				.eq("id", invitation.id);

			return res.status(409).json({
				success: false,
				message: "You are already a member of this project.",
			});
		}

		// Add member and mark invitation accepted in parallel
		const [memberResult, inviteResult] = await Promise.all([
			supabase.from("project_members").insert({
				project_id: invitation.project_id,
				user_id: profile.id,
				role: invitation.role,
			}),
			supabase
				.from("invitations")
				.update({ status: "accepted", accepted_at: new Date().toISOString() })
				.eq("id", invitation.id),
		]);

		if (memberResult.error) throw memberResult.error;
		if (inviteResult.error) throw inviteResult.error;

		res.status(200).json({
			success: true,
			message: "Invitation accepted. You have been added to the project.",
			data: {
				project_id: invitation.project_id,
				role: invitation.role,
			},
		});
	} catch (error) {
		next(error);
	}
}
